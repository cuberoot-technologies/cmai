	<h4 class="mt-4 text-xl text-green-500 md:text-2xl">From Stockholm to Kyoto</h4>
	<p class="text-l mt-4 text-gray-600">
		As we look at the rise in prominence of environmental issues and the evolution of climate change
		within that context, the UN Scientific Conference, also known as the First Earth Summit, held in
		Stockholm, Sweden in 1972 was the first international multilateral effort to focus on the human
		impact on the environment. The conference adopted an action plan containing recommendations for
		international environmental action.
		<br />
		<br />

		But it took 20 years to act on the plan as it was in 1992 that a Convention on Climate Change
		was formed which was more popularly known as the Rio Earth Summit. The multilateral
		environmental treaty signed at the Rio Earth Summit by delegates from over 154 countries
		provided the Framework Convention on Climate Change (FCCC) which came into force in 1994. The
		objective of the Convention was to control the concentration of greenhouse gases in the
		atmosphere at levels that would ensure that there is no dangerous anthropogenic interference in
		the climate system.
		<br />
		<br />

		It was in March 1995 that the first Conference of the Parties (CoP) to the Convention adopted
		the Berlin Mandate containing stronger commitments by developed countries. The most influential
		climate change action taken thereafter was the adoption of the Kyoto Protocol in Japan in 1997
		during COP3 which had over 150 nation signatories. It aimed to reduce the overall emissions of
		greenhouse gases by at least 5 percent below the 1990 levels between 2008 – 2012.
		<br />
		<br />

		The Kyoto Protocol also laid down the foundation of Market Based Instruments (MBIs) for emission
		reduction, most significantly the Clean Development Mechanism (CDM) which allowed a country with
		an emission reduction commitment to develop a project in a developing country to earn certified
		emission reduction (CER) credits to meet Kyoto targets. The Kyoto Protocol set targets for the
		developed world while developing countries were not required to make any commitments.
		<br />
		<br />

		These targets were unfortunately never met as the market collapsed due to concerns over double
		counting and environmental efficacy, corruption, and quality of emission reductions, leading to
		an end of the initial era of compliance markets.
		<br />
		<br />

		As of today, an increasing number of nations are committing to turn the 2020s into a decade of
		significant climate action. COP26 held in 2021 at Glasgow witnessed the momentum of countries to
		achieve net zero including India’s pledge to achieve net-zero by 2070. We are enabling this
		journey of climate action with our significant efforts to develop India’s carbon markets and
		ensure a sustainable tomorrow.
	</p>

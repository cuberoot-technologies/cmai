		The COP is the supreme decision-making body that reviews the national communications and
		emission inventories submitted by Parties (member States & countries) along with other legal
		instruments that the COP adopts to take decisions necessary to promote the effective
		implementation of the Convention.
		<br />
		<br />
		The COP meets every year, unless the Parties decide otherwise. The first COP meeting was held in
		Berlin, Germany in March, 1995. Since then, the countries bound by UNFCCC have met every year except
		2020 to discuss climate change mitigation measures and produce strategic action plans including Kyoto
		Protocol 1997 and Paris 2015 amongst others. As many as 200 world leaders participated in the 26th
		edition of COP held in Glasgow, Scotland in 2021. COP26 witnessed the zeal of nations towards climate
		action and support as many countries set targets of net-zero emissions including Indiaâ€™s pledge to
		achieve net-zero by 2070. The two weeks of tense talks also resulted in several important decisions
		in the collective effort to keep the 1.5C warming limit within reach.
		<br />
		<br />
		The Glasgow Climate Pact consists of a range of agreed items, including strengthened efforts to curb
		greenhouse gas emissions, build resilience to climate change, and provide the necessary finance for
		both. One of the key focus areas at COP26 was to make Paris Agreement operational through completion
		of the Paris Agreement rule book.
		<br />
		<br />
		The Paris Agreement 2015 was adopted at COP21, aiming to limit global warming to well below 2 degrees
		Celsius, preferably to 1.5, compared to pre-industrial levels. It gave impetus to net-zero commitments
		and COP26 further accelerated the momentum. Article 6 of the Paris Agreement was a primary focus
		of COP26. The Article 6 rules give countries the tools they need for environmental integrity, to
		avoid double-counting through corresponding adjustment mechanism, and to ensure that private capital
		flows to developing countries.
		<br />
		<br />
		Nations agreed to form an action plan to reduce the gap between existing emission reduction plans
		and an ideal plan to reduce emissions. They also reaffirmed their pledge to provide 100 billion dollars
		annually from developed to developing nations. COP26 also saw a significant move by UNFCCC as it
		was for the first time that nations were called upon to cut down inefficient subsidies for fossil
		fuels and unabated coal power.

	<p class="text-l mt-4 text-gray-600">
		Carbon credit is an offset solution that compensates for the emission of carbon dioxide
		emissions into the atmosphere by preventing or reducing the same amount of carbon dioxide from
		entering the atmosphere elsewhere on earth through green projects/initiatives.
		<br />
		<br />

		India has accumulated carbon credits through its participation in the Clean Development
		Mechanism (CDM) projects. The abundance of CDM projects mean India has been one of the largest
		recipients of tradable Certified Emission Reduction (CER) credits. The CDM and CER together has
		helped India in laying the groundwork for the development of a Voluntary Carbon Credit Market
		(VCM).
		<br />
		<br />

		The now proposed amendments to the Energy Conservation Act 2022 Act is now set to empower the
		development of India’s carbon market that will in turn enable the country to transition into a
		low carbon economy. With the proposed amendments, the current trading schemes in India – Energy
		Saving Certificates (ESCerts) and Renewable Energy Certificates (RECs) will be merged into one
		single commodity that will be called Carbon Credits Certificate (CCC) and will operate under Cap
		& Trade system under the National ETS.
		<br />
		<br />

		Carbon Markets Association of India and our consortium of industry experts aims to enable this
		growth of India’s carbon credit market with concrete measures to overcome industry challenges
		even as we consistently work towards increasing industry participation for policy advocacy and
		framework guidelines. A robust market will ensure a steady flow of carbon credits and this would
		translate into more green projects in the country for increased mitigation of carbon dioxide
		from entering the atmosphere. Together, this will empower the country’s journey to net-zero.
	</p>
